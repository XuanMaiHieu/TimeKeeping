package com.example.timekeeping;

public class GioDaLam {

    private int ten;
    private int giodalam;

    public GioDaLam(int ten, int giodalam) {
        this.ten = ten;
        this.giodalam = giodalam;
    }

    public int getTen() {
        return ten;
    }

    public void setTen(int ten) {
        this.ten = ten;
    }

    public int getGiodalam() {
        return giodalam;
    }

    public void setGiodalam(int giodalam) {
        this.giodalam = giodalam;
    }
}
